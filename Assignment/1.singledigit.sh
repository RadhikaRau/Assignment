
echo "Enter a Single digit Number"
read number

if [ $number -eq 1 ];
then
 echo "Number is one"
elif [ $number -eq 2 ];
then
 echo "Number is two"
elif [ $number -eq 3 ];
then
 echo "Number is three"
elif [ $number -eq 4 ];
then
 echo "Number is four"
elif [ $number -eq 5 ];
then
 echo "Number is five"
elif [ $number -eq 6 ];
then
 echo "Number is six"
elif [ $number -eq 7 ];
then
 echo "Number is seven"
elif [ $number -eq 8 ];
then
 echo "Number is eight"
elif [ $number -eq 9 ];
then
 echo "Number is nine"
else
echo " Invalid input "
fi
